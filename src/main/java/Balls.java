public class Balls
{
    private int ball_no;
    private int height_of_ball;
    private int speed_of_ball;
    private int radius;

    public int getBall_no() {
        return ball_no;
    }

    public void setBall_no(int ball_no) {
        this.ball_no = ball_no;
    }

    public int getHeight_of_ball() {
        return height_of_ball;
    }

    public void setHeight_of_ball(int height_of_ball) {
        this.height_of_ball = height_of_ball;
    }

    public int getSpeed_of_ball() {
        return speed_of_ball;
    }

    public void setSpeed_of_ball(int speed_of_ball) {
        this.speed_of_ball = speed_of_ball;
    }

    public int getRadius() {
        return radius;
    }

    public void setRadius(int radius) {
        this.radius = radius;
    }

}
